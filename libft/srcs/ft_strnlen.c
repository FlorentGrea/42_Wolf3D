#include "../includes/libft.h"

size_t		ft_strnlen(char *s, int c)
{
	size_t		i;
	char		ca;

	i = 0;
	ca = (char)c;
	while (s[i] != '\0' && s[i] != ca)
		i++;
	return (i);
}
