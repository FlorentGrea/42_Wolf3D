#include "../includes/libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s3;
	size_t	i;
	size_t	y;

	y = 0;
	s3 = s1;
	i = ft_strlen(s1);
	while (s2[y] && y < n)
	{
		s1[i] = s2[y];
		i++;
		y++;
	}
	s1[i] = '\0';
	return (s3);
}
