#include "../includes/libft.h"

char	*ft_strdup(const char *s1)
{
	size_t		i;
	char		*dest;

	i = ft_strlen(s1);
	if ((dest = (char *)malloc(sizeof(*dest) * i + 1)) == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
